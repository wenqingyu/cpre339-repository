
public abstract class Genre {
	
	/**
	 * set category name of this movie
	 * @param category
	 */
	abstract void SetCategory(String category);
	
	/**
	 * get category name
	 * @return
	 */
	abstract String getCategory();
	
	/**
	 * 
	 * @return this genre name
	 */
	abstract String getGenreName();
	
	
	
}
